# Q3: Explain the concept of Type 1 and Type 2 errors in hypothesis testing. Provide an example scenario for
# each type of error.

# Answer - 

# Type 1 error - type 1 error caused when we reject the null hypothesis and in reality its true.
# for Example - convict someone of a crime when they are actually innocent.

# Type 2 error - type 2 error caused when we retain the null hypotheis and in reality its false.
# for Example - test for a disease may report a negative result when the patient is infected